/*
 * Terrarium guest: TypeScript — checked, stripped, and run inside the sandbox.
 *
 * QuickJS-ng with the real TypeScript compiler embedded as precompiled QuickJS
 * bytecode. Each eval: fetch the host-generated SDK `.d.ts` (reserved "$dts"
 * cap), type-check the submitted source against it (strict; skipped by a
 * leading `// @ts-nocheck`), erase the types whitespace-preserving
 * (ts-blank-space, so runtime error lines match the TS source exactly), then
 * evaluate the resulting JavaScript exactly like the plain QuickJS guest.
 *
 * Two contexts, one lifetime rule:
 *  - the "compiler" JSRuntime/JSContext (TypeScript + libs + driver) is created
 *    once per wasm instance and persists across evals — lib parsing and Program
 *    state amortize in shared mode;
 *  - the "user" JSRuntime/JSContext is fresh per eval, identical to the plain
 *    QuickJS guest.
 *
 * Same host ABI as every other guest:
 *   - exports `memory` and `guest_alloc(len) -> ptr` (host->guest writes)
 *   - exports `eval(ptr, len) -> packed` where the argument is a msgpack string
 *     (the TS source) and the result is `(retPtr << 32) | retLen` (msgpack)
 *   - imports host.host_call(name_ptr, name_len, args_ptr, args_len) -> packed
 *
 * A check failure comes back as the standard sentinel
 * { "$error": {message: "TS2345: ...", type: "TS2345", line} }.
 *
 * Built with the WASI SDK in reactor mode; see build.sh.
 */
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <math.h>
#include "quickjs.h"

