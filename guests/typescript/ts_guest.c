/* the persistent compiler context                                     */
/* ------------------------------------------------------------------ */

static JSRuntime *g_crt = NULL;
static JSContext *g_cctx = NULL;

static int eval_bytecode(JSContext *ctx, const uint8_t *buf, uint32_t len) {
    JSValue obj = JS_ReadObject(ctx, buf, len, JS_READ_OBJ_BYTECODE);
    if (JS_IsException(obj)) return 1;
    JSValue res = JS_EvalFunction(ctx, obj);
    if (JS_IsException(res)) return 1;
    JS_FreeValue(ctx, res);
    return 0;
}

/* Bring up TypeScript + libs + ts-blank-space + the driver, once per instance.
 * The compiler runtime persists across evals: in shared mode the lib parse and
 * Program caches amortize; isolated mode pays this once per (fresh) instance. */
static int ensure_compiler(void) {
    if (g_cctx) return 0;
    g_crt = JS_NewRuntime();
    if (!g_crt) return 1;
    /* The checker recurses deeply; give it a generous JS-level stack budget
     * (the wasm linker stack is sized to cover it — see build.sh). */
    JS_SetMaxStackSize(g_crt, 4 * 1024 * 1024);
    g_cctx = JS_NewContext(g_crt);
    if (!g_cctx) return 1;

    /* typescript.js is a CommonJS bundle: give it module/exports + a stub
     * process before evaluating its bytecode. */
    static const char CPRELUDE[] =
        "globalThis.module = { exports: {} };"
        "globalThis.exports = globalThis.module.exports;"
        "globalThis.process = { env: {}, argv: [], versions: {}, cwd: function(){ return '/'; } };";
    JSValue pre = JS_Eval(g_cctx, CPRELUDE, sizeof(CPRELUDE) - 1, "<cprelude>", JS_EVAL_TYPE_GLOBAL);
    if (JS_IsException(pre)) return 1;
    JS_FreeValue(g_cctx, pre);

    if (eval_bytecode(g_cctx, qjsc_typescript, qjsc_typescript_size)) return 1;

    static const char EXPORT_TS[] = "globalThis.ts = globalThis.module.exports;";
    JSValue ex = JS_Eval(g_cctx, EXPORT_TS, sizeof(EXPORT_TS) - 1, "<cprelude>", JS_EVAL_TYPE_GLOBAL);
    if (JS_IsException(ex)) return 1;
    JS_FreeValue(g_cctx, ex);

    if (eval_bytecode(g_cctx, qjsc_libs, qjsc_libs_size)) return 1;
    if (eval_bytecode(g_cctx, qjsc_tsblank, qjsc_tsblank_size)) return 1;
    if (eval_bytecode(g_cctx, qjsc_driver, qjsc_driver_size)) return 1;
    return 0;
}

