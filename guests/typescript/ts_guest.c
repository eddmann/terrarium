/* Wizer pre-initialization                                            */
/* ------------------------------------------------------------------ */

/* Wizer (build.sh, final step) calls this once, offline: it brings up the
 * compiler context — reads the ~2 MB TypeScript bytecode, evaluates it into a
 * live compiler object graph, parses the lib .d.ts chain + ts-blank-space + the
 * driver — then snapshots the resulting linear memory and globals into the
 * module's data segments. At runtime `g_cctx` is therefore already non-null, so
 * `ensure_compiler()` short-circuits on its first line: the ~1 s bootstrap is
 * paid at build time, once, and every instance (shared or a fresh isolated one)
 * starts with the compiler already warm.
 *
 * The snapshot is self-contained: `ensure_compiler()` makes no host calls (the
 * SDK .d.ts is fetched per-eval, not here), so Wizer needs no working `host`
 * import — a trapping stub suffices, and none of it fires. If bring-up fails we
 * abort rather than bake a half-initialized heap. */
__attribute__((export_name("wizer.initialize")))
void wizer_initialize(void) {
    if (ensure_compiler()) abort();

    /* Bring up the compiler object graph is not enough on its own: the ~500 ms
     * a first eval pays is `ts.createProgram` *parsing and binding the ~88 lib
     * .d.ts files*, and that happens on the first check in any fresh compiler
     * context. So run one throwaway check here too. The driver caches parsed +
     * bound lib SourceFiles (`libCache`) for the context's life; those are
     * SDK-independent, so baking them means every eval — including each fresh
     * isolated instance — reuses the warm libs instead of re-parsing them.
     * (The per-SDK Program cache can't carry over — the real SDK .d.ts is only
     * known per-eval — but the lib parse/bind is the dominant cost.) */
    static const char WARM[] =
        "__terrariumCompile('const __warm: number = 1; __warm;', '');";
    JSValue r = JS_Eval(g_cctx, WARM, sizeof(WARM) - 1, "<warmup>", JS_EVAL_TYPE_GLOBAL);
    if (JS_IsException(r)) abort();
    JS_FreeValue(g_cctx, r);
}

/* Fetch the SDK .d.ts from the host (reserved "$dts" cap) as a JSValue string
 * in the compiler context. */
static JSValue fetch_dts(JSContext *ctx) {
    static const char NAME[] = "$dts";
    Buf ab = {0};
    enc_arr_hdr(&ab, 0);
    int64_t packed = host_call((int32_t)(intptr_t)NAME, 4,
                               (int32_t)(intptr_t)ab.data, (int32_t)ab.len);
    free(ab.data);
    uint32_t rptr = (uint32_t)(packed >> 32);
    uint32_t rlen = (uint32_t)(packed & 0xffffffff);
    Rd r = { (const uint8_t *)(uintptr_t)rptr, 0, rlen };
    JSValue dts = mp_to_js(ctx, &r);
    free((void *)(uintptr_t)rptr);
    return dts;
}

/* ------------------------------------------------------------------ */
/* check entrypoint (optional export — type-check only, nothing runs)  */
/* ------------------------------------------------------------------ */

/* `check(ptr, len)`: the argument is a msgpack string (TS source). Returns a
 * msgpack ARRAY of every error diagnostic ({message, type, line?}); an empty
 * array means the source is well-typed against the current SDK. No user
 * context is created and no guest code executes. Ignores @ts-nocheck — an
 * explicit check asks for the diagnostics. */
__attribute__((export_name("check")))
int64_t check(int32_t ptr, int32_t len) {
    if (ensure_compiler()) {
        return ret_error("failed to start the TypeScript compiler");
    }

    Rd r = { (const uint8_t *)(uintptr_t)(uint32_t)ptr, 0, (size_t)(uint32_t)len };
    JSValue srcv = mp_to_js(g_cctx, &r);
    if (!JS_IsString(srcv)) {
        JS_FreeValue(g_cctx, srcv);
        return ret_error("check expects a source string");
    }

    JSValue g = JS_GetGlobalObject(g_cctx);
    JSValue fn = JS_GetPropertyStr(g_cctx, g, "__terrariumCheck");
    JS_FreeValue(g_cctx, g);
    JSValue dts = fetch_dts(g_cctx);
    JSValue args[2] = { srcv, dts };
    JSValue res = JS_Call(g_cctx, fn, JS_UNDEFINED, 2, args);
    JS_FreeValue(g_cctx, fn);
    JS_FreeValue(g_cctx, srcv);
    JS_FreeValue(g_cctx, dts);

    int64_t out;
    if (JS_IsException(res)) {
        JSValue exc = JS_GetException(g_cctx);
        const char *s = JS_ToCString(g_cctx, exc);
        char msgbuf[512];
        snprintf(msgbuf, sizeof(msgbuf), "TypeScript compiler error: %s", s ? s : "unknown");
        if (s) JS_FreeCString(g_cctx, s);
        JS_FreeValue(g_cctx, exc);
        out = ret_error(msgbuf);
    } else {
        out = ret_value(g_cctx, res);
    }
    JS_FreeValue(g_cctx, res);
    return out;
}

/* ------------------------------------------------------------------ */
/* eval entrypoint                                                     */
/* ------------------------------------------------------------------ */

__attribute__((export_name("eval")))
int64_t eval(int32_t ptr, int32_t len) {
    /* The argument is a msgpack string: the TypeScript source. Decode it in a
     * fresh user runtime (also used to run the stripped JS afterwards). */
    JSRuntime *rt = JS_NewRuntime();
    if (!rt) return ret_error("failed to create JS runtime");
    JS_SetMaxStackSize(rt, 256 * 1024);
    JSContext *ctx = JS_NewContext(rt);

    Rd r = { (const uint8_t *)(uintptr_t)(uint32_t)ptr, 0, (size_t)(uint32_t)len };
    JSValue srcv = mp_to_js(ctx, &r);
    size_t slen; const char *src = JS_ToCStringLen(ctx, &slen, srcv);

    int64_t out;
    if (!src) {
        out = ret_error("eval expects a source string");
        goto done_user_only;
    }

    /* --- compile: check (unless @ts-nocheck) + strip, in the compiler ctx --- */
    if (ensure_compiler()) {
        out = ret_error("failed to start the TypeScript compiler");
        goto done_src;
    }
    {
        JSValue g = JS_GetGlobalObject(g_cctx);
        JSValue fn = JS_GetPropertyStr(g_cctx, g, "__terrariumCompile");
        JS_FreeValue(g_cctx, g);
        JSValue csrc = JS_NewStringLen(g_cctx, src, slen);
        JSValue dts = fetch_dts(g_cctx);
        JSValue args[2] = { csrc, dts };
        JSValue res = JS_Call(g_cctx, fn, JS_UNDEFINED, 2, args);
        JS_FreeValue(g_cctx, fn);
        JS_FreeValue(g_cctx, csrc);
        JS_FreeValue(g_cctx, dts);

        if (JS_IsException(res)) {
            JSValue exc = JS_GetException(g_cctx);
            const char *s = JS_ToCString(g_cctx, exc);
            char msgbuf[512];
            snprintf(msgbuf, sizeof(msgbuf), "TypeScript compiler error: %s", s ? s : "unknown");
            if (s) JS_FreeCString(g_cctx, s);
            JS_FreeValue(g_cctx, exc);
            out = ret_error(msgbuf);
            goto done_src;
        }

        JSValue jerr = JS_GetPropertyStr(g_cctx, res, "error");
        if (JS_IsObject(jerr)) {
            /* A check / syntax failure: forward {message, type, line}. */
            JSValue jm = JS_GetPropertyStr(g_cctx, jerr, "message");
            JSValue jt = JS_GetPropertyStr(g_cctx, jerr, "type");
            JSValue jl = JS_GetPropertyStr(g_cctx, jerr, "line");
            const char *m = JS_ToCString(g_cctx, jm);
            const char *t = JS_IsString(jt) ? JS_ToCString(g_cctx, jt) : NULL;
            int has_line = JS_IsNumber(jl);
            int64_t line = 0;
            if (has_line) JS_ToInt64(g_cctx, &line, jl);
            out = ret_error_full(m ? m : "type check failed", t, has_line, line);
            if (m) JS_FreeCString(g_cctx, m);
            if (t) JS_FreeCString(g_cctx, t);
            JS_FreeValue(g_cctx, jm);
            JS_FreeValue(g_cctx, jt);
            JS_FreeValue(g_cctx, jl);
            JS_FreeValue(g_cctx, jerr);
            JS_FreeValue(g_cctx, res);
            goto done_src;
        }
        JS_FreeValue(g_cctx, jerr);

        JSValue jjs = JS_GetPropertyStr(g_cctx, res, "js");
        size_t jslen; const char *js = JS_ToCStringLen(g_cctx, &jslen, jjs);
        JS_FreeValue(g_cctx, res);
        if (!js) {
            JS_FreeValue(g_cctx, jjs);
            out = ret_error("TypeScript compiler returned no output");
            goto done_src;
        }

        /* --- run the stripped JS in the fresh user context --- */
        JSValue global = JS_GetGlobalObject(ctx);
        JS_SetPropertyStr(ctx, global, "__host",
                          JS_NewCFunction(ctx, js_host, "__host", 1));
        JS_FreeValue(ctx, global);
        JSValue pre = JS_Eval(ctx, PRELUDE, strlen(PRELUDE), "<prelude>", JS_EVAL_TYPE_GLOBAL);
        JS_FreeValue(ctx, pre);

        JSValue ures = JS_Eval(ctx, js, jslen, "<eval>", JS_EVAL_TYPE_GLOBAL);
        JS_FreeCString(g_cctx, js);
        JS_FreeValue(g_cctx, jjs);

        if (JS_IsException(ures)) {
            JSValue exc = JS_GetException(ctx);
            out = ret_error_exc(ctx, exc);
            JS_FreeValue(ctx, exc);
        } else {
            out = ret_value(ctx, ures);
        }
        JS_FreeValue(ctx, ures);
    }

done_src:
    JS_FreeCString(ctx, src);
done_user_only:
    JS_FreeValue(ctx, srcv);
    JS_FreeContext(ctx);
    JS_FreeRuntime(rt);
    return out;
}
